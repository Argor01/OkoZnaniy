class Command(BaseCommand):
    help = 'Загружает список навыков (skills) в базу данных'

    def handle(self, *args, **kwargs):
        created_count = 0
        existing_count = 0

        for name in SKILLS:
            name = name.strip()
            if not name:
                continue
            _, created = Skill.objects.get_or_create(name=name)
            if created:
                created_count += 1
            else:
                existing_count += 1

        self.stdout.write(
            self.style.SUCCESS(
                f'Готово: создано {created_count}, уже существовало {existing_count}. '
                f'Всего навыков: {Skill.objects.count()}'
            )
        )
